package com.wangshuai.androidui.anim;


public class Point {

    private float x;//x轴坐标值
    private float y;//y轴坐标值

    public Point(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
